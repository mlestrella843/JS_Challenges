// Reversing an array

const fruits = ["Mango", "Grape", "Apple", "Pineapple"]

const reverseFruits = fruits.reverse();

console.log(reverseFruits); // ["Pineapple", "Apple", "Grape", "Mango"]


// Reversing a string

const word = "Hello"

console.log(word)

const reverseWorld = word.split("");

console.log(reverseWorld);

const reverseWorld1 = reverseWorld.reverse();

console.log(reverseWorld1);

const reverseWord2 = reverseWorld1.join("");

console.log(reverseWord2); // "olleH"